    @Test
    @Transactional
    void getAllReservations() throws Exception {
        // Initialize the database
        reservationRepository.saveAndFlush(reservation);

        // Get all the reservationList
        restReservationMockMvc
            .perform(get(ENTITY_API_URL + "?sort=id,desc"))
            .andExpect(status().isOk())
            .andExpect(content().contentType(MediaType.APPLICATION_JSON_VALUE))
            .andExpect(jsonPath("$.[*].id").value(hasItem(reservation.getId().intValue())))
            .andExpect(jsonPath("$.[*].reservedTime").value(hasItem(sameInstant(DEFAULT_RESERVED_TIME))))
            .andExpect(jsonPath("$.[*].reservedExpiry").value(hasItem(sameInstant(DEFAULT_RESERVED_EXPIRY))))
            .andExpect(jsonPath("$.[*].status").value(hasItem(DEFAULT_STATUS.toString())));
    }

    @Test
    @Transactional
    void getReservation() throws Exception {
        // Initialize the database
        reservationRepository.saveAndFlush(reservation);

        // Get the reservation
        restReservationMockMvc
            .perform(get(ENTITY_API_URL_ID, reservation.getId()))
            .andExpect(status().isOk())
            .andExpect(content().contentType(MediaType.APPLICATION_JSON_VALUE))
            .andExpect(jsonPath("$.id").value(reservation.getId().intValue()))
            .andExpect(jsonPath("$.reservedTime").value(sameInstant(DEFAULT_RESERVED_TIME)))
            .andExpect(jsonPath("$.reservedExpiry").value(sameInstant(DEFAULT_RESERVED_EXPIRY)))
            .andExpect(jsonPath("$.status").value(DEFAULT_STATUS.toString()));
    }

